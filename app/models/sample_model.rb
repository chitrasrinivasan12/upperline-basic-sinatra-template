def health_generator(user_value)
  
  if user_value == 22
    return "op1"
  elsif user_value == 25
    return "op2"
  elsif user_value <= 16
    return "op3"
  elsif user_value <= 108
    return "op4"
    elsif user_value == 112
    return "op5"
  elsif user_value == 115
    return "op6"
  end
  
end
